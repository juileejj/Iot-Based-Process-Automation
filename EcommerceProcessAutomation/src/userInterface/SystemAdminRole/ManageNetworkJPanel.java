    private void btnSubmitPanelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitPanelActionPerformed
        try {
            if (stateSelectCombobox.getSelectedItem().equals("--Select")) {
                JOptionPane.showMessageDialog(this, "Error Message", "Please select a state", JOptionPane.ERROR_MESSAGE);
            } else {
                Network n = (Network) stateSelectCombobox.getSelectedItem();
                Network city = n.addCity(n);
                city.setCityName(txtCityName.getText());
                city.setZipCode(Integer.parseInt(txtZipCode.getText()));
                populateLocationTable(n);
                stateViewCombobox.setSelectedItem(n);
                JOptionPane.showMessageDialog(this, "Location- " + city.getCityName() + " added successfully", "Success Message", JOptionPane.INFORMATION_MESSAGE);
                txtCityName.setText("");stateSelectCombobox.setSelectedIndex(0);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Attempt unsuccessful", "Error Message",  JOptionPane.ERROR_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSubmitPanelActionPerformed

