    private void btnDeleteLocationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteLocationActionPerformed
        int selectedRow= networkJTable.getSelectedRow();
if(selectedRow>=0)
{
int dialogButton = JOptionPane.YES_NO_OPTION;
int dialogResult= JOptionPane.showConfirmDialog(null,"Would you like to delete this network?","Warning",dialogButton);
if(dialogResult==JOptionPane.YES_OPTION)
{
Network network =(Network)stateViewCombobox.getSelectedItem();
String cityName = networkJTable.getValueAt(selectedRow,0).toString();
int zipCode = Integer.parseInt(networkJTable.getValueAt(selectedRow,1).toString());
Network city = network.getCityObjectFromName(cityName, network, zipCode);
network.removeCity(network, city);
    populateLocationTable(network);
//resetField()
}
}else
{
JOptionPane.showMessageDialog(null,"Please select a row first");
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteLocationActionPerformed


