    public void populateLocationTable(Network n) {

        DefaultTableModel model = (DefaultTableModel) networkJTable.getModel();

        model.setRowCount(0);
        for (Network city : n.getCityList()) {
            Object[] row = new Object[2];
            row[0] = city.toString(city);
            row[1] = city.getZipCode();
            model.addRow(row);
        }

    }

